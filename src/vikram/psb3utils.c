static uint8_t _PSB3_parityHelper (uint8_t a, uint8_t b)
{
    if ((a == b) || (a == 0) || (b == 0)) {
        return a ^ b;
    }

    uint16_t x;
    if (b > a) {
        x = b - a - 1;
    } else {
        x = 254 + b - a;
    }

    x = _PSB3_parity2[x] + a;
    if (x >= 256) {
        x -= 255;
    }

    return x;
}


/* Check parity bits */
_Bool _PSB3_checkParity (uint8_t *buffer, int length)
{
    (void)length;
    bool result = true;
    int i, j;
    uint8_t fec[10];

    memset(fec, 0, sizeof(fec));

    for (i = 0; i < 35; i++) {
        uint8_t x = _PSB3_parityHelper(fec[0], buffer[i]);
        uint16_t w;
        for (j = 0; j < 9; j++) {
            w = x + _PSB3_parity1[j];
            if (w >= 256) {
                w -= 255;
            }
            fec[j] = _PSB3_parityHelper(w & 0xFF, fec[j+1]);
        }
    }

    for (i = 0; i < 9; i++) {
        if (fec[i] != buffer[35+i]) {
            result = false;
        }
    }

    return result;
}


/* Check frame CRC */
_Bool _PSB3_checkCRC (uint8_t *buffer, int length, uint16_t receivedCRC)
{
    CRC_Handle crc = LPCLIB_INVALID_HANDLE;
    CRC_Mode crcMode;
    bool result = false;

    crcMode = CRC_makeMode(
            CRC_POLY_CRC16,
            CRC_DATAORDER_NORMAL,
            CRC_SUMORDER_NORMAL,
            CRC_DATAPOLARITY_NORMAL,
            CRC_SUMPOLARITY_NORMAL
            );
    if (CRC_open(crcMode, &crc) == LPCLIB_SUCCESS) {
        CRC_seed(crc, 0x0000);
        CRC_write(crc, buffer, length, NULL, NULL);

        result = receivedCRC == CRC_read(crc);

        CRC_close(&crc);
    }

    return result;
}
